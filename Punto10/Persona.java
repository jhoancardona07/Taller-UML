package Punto10;

public class Persona {

    private String nombre;
    private int edad;

    public Persona() {}

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String val) {
        nombre = val;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int val) {
        edad = val;
    }
}